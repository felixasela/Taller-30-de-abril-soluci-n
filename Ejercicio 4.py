# Se solicitan 2 números al usuario
a = int(input("Digite un número: "))
b = int(input("Digite un número: "))
# Suma de dos números ingresados por el usuario
suma = a + b
# Resta de dos números ingresados por el usuario
resta = a - b
# Multiplicación de dos números ingresados por el usuario
multiplicacion = a * b
# División de dos números ingresados por el usuario
division = a / b
# Residuo de dos números ingresados por el usuario
residuo = a % b
print("La suma de los dos números digitados es = " + str(suma))
print("La resta de los dos números digitados es = " + str(resta))
print("La multiplicación de los dos números digitados es = " + str(multiplicacion))
print("La división de los dos números digitados es = " + str(division))
print("El residuo de los dos números digitados es = " + str(residuo))